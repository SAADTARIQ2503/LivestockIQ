# --- NEW AJAX ENDPOINT FOR VACCINE FILTERING ---
def get_vaccines_by_species_for_animal_add(request):
    """
    Returns a JSON list of unique vaccine names filtered by species (animal type).
    Used by the add_animal form for the 'Required Vaccine' dropdown.
    """
    species = request.GET.get('species')
    
    if not species:
        return JsonResponse([], safe=False)

    # Filter VaccineDataset by the provided species
    vaccines = VaccineDataset.objects.filter(
        species__iexact=species
    ).values_list('vaccine_name', flat=True).distinct().order_by('vaccine_name')
    
    # Convert queryset to list and return as JSON
    return JsonResponse(list(vaccines), safe=False)
